    乾“元”者，始而亨者也；“利贞”者，性情也。乾始能以美利利天下，不言所利，大矣哉，大哉乾乎，刚健中正，纯粹精也。六爻发挥，旁通情也，时乘六龙，以御天也。云行雨施，天下平也。君子以成德为行，日可见之行也。
    “潜”之为言也，隐而未见，行而未成，是以君子弗用也。君子学以聚之，问以辩之，宽以居之，仁以行之。《易》曰：“见龙在田，利见大人。”君德也。
