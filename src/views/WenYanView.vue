  <h2>坤卦</h2>

  <p>
    坤至柔而动也刚，至静而德方，后得主而有常，含万物而化光。坤道其顺乎，承天而时行。积善之家必有余庆，积不善之家必有余殃。
    臣弑其君，子弑其父，非一朝一夕之故，其所由来者渐矣。由辩之不早辩也。
    《易》曰：“履霜，坚冰至。”盖言顺也。“直”其正也，“方”其义也。君子敬以直内，义以方外，敬义立而德不孤。
    “直方大，不习无不利。”则不疑其所行也。阴虽有美，“含”之以从王事，弗敢成也。地道也，妻道也，臣道也。地道“无成”而代“有终”也。天地变化，草木蕃，天地闭，贤人隐。
    《易》曰：“括囊，无咎无誉。”盖言谨也。君子“黄”中通理，正位居体，美在其中，而畅于四支，发于事业，美之至也！
    阴疑于阳必战，为其嫌于无阳也。故称“龙”焉犹未离其类也，故称“血”焉。夫“玄黄”者，天地之杂也，天玄而地黄。
